void main(){
  // For loop untuk mencetak angka 1 sampai 5
  for (int i = 1; i <= 5; i++) {
  print(i);
  }

  // While loop untuk mencetak angka 1 sampai 10
  int j = 1;  // inisialisasi variabel
  while (j <= 10) {
  print('Angka: $j');
  j++; // Increment untuk menghindari loop tak berujung
  }
}